// Helper function to get project slug from title
export const getProjectSlug = (title) => {
  return title.toLowerCase()
    .replace(/®/g, '')
    .replace(/&/g, 'and')
    .replace(/\s+/g, '-')
    .replace(/[^a-z0-9-]/g, '')
}

